void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART3)
  { 
	  if(RxDataChar[0] == 'u')
		{
			Set_Update_Firm_Completed(0x35);
		}
		else if(RxDataChar[0] == 'n')
		{
			Set_Update_Firm_Completed(0x37);
		}
		else {
			if (RxDataChar[0] == HeaderString[RxCmdIndex] || RxCmdIndex > 2) {			// Check data has header 'EB-'
				RxCmdString[RxCmdIndex] = RxDataChar[0];
				RxCmdIndex++;
				if(RxDataChar[0] == ';' || RxCmdIndex == 5) { // current receive each line
					receive_cmd = 1;
					RxCmdIndex = 0;
				}
			} else {
				memset(RxCmdString, 0, sizeof(RxCmdString));
				RxCmdString[0] = RxDataChar[0];
				RxCmdIndex = 1;
			}
		}
		HAL_UART_Receive_IT(&huart3, (uint8_t*)RxDataChar, 1);
  }
}
